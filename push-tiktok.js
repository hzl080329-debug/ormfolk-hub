const items = videos.map(v => {
  const [vid, ...rest] = v.split("|");
  const title = rest.join("|");
  return {
    url: "https://www.tiktok.com/@omeormsin/video/" + vid,
    caption: title,
    tag: "orm",
    category: "tiktok",
  };
});

async function push() {
  let count = 0;
  for (let i = 0; i < items.length; i += 20) {
    const batch = items.slice(i, i + 20);
    const res = await fetch("/api/admin/batch-gallery", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ items: batch }),
    });
    const data = await res.json();
    count += data.count || 0;
    console.log("Batch " + (i/20+1) + ": " + data.count + " items");
  }
  console.log("Done! Total: " + count);
}

push();
